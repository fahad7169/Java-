import java.util.*;

class year{
	
	public int getyear(int year){
		return 2023-year;
		
		
		
	}
	
	
	
}



public class Task5{
	public static void main(String args[]){
		year y1=new year();
		Scanner sc=new Scanner(System.in);
		
		System.out.println("Enter the birth year: ");
		int year=sc.nextInt();
		System.out.println("Your age is: "+y1.getyear(year));
		
		
		
	}
	
	
	
}